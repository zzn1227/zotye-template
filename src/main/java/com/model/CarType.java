package com.model;

public class CarType {

    private String  carTypeNum;
    private String  carTypeName;
    private Integer factoryNum;

    public String getCarTypeNum() {
        return carTypeNum;
    }

    public void setCarTypeNum(String carTypeNum) {
        this.carTypeNum = carTypeNum;
    }

    public String getCarTypeName() {
        return carTypeName;
    }

    public void setCarTypeName(String carTypeName) {
        this.carTypeName = carTypeName;
    }

    public Integer getFactoryNum() {
        return factoryNum;
    }

    public void setFactoryNum(Integer factoryNum) {
        this.factoryNum = factoryNum;
    }

}
